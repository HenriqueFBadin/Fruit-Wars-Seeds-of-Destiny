        public static float GetEffectiveness(MoveBase.PokemonType attackType, PokemonType defenseType)
        {
            if (attackType == MoveBase.PokemonType.None || defenseType == PokemonType.None)
            {
                return 1;
            }

            int row = (int)attackType - 1;
            int col = (int)defenseType - 1;

            return chart[row][col];
        }
    }

    public int GetExpForLevel(int level, GrowthRate growthRate)
    {
        switch (growthRate)
        {
            case GrowthRate.Fast:
                return Mathf.FloorToInt(4 * Mathf.Pow(level, 3) / 5);
            case GrowthRate.MediumFast:
                return Mathf.FloorToInt(Mathf.Pow(level, 3));
            case GrowthRate.MediumSlow:
                return Mathf.FloorToInt(6f / 5f * Mathf.Pow(level, 3) - 15 * Mathf.Pow(level, 2) + 100 * level - 140);
            case GrowthRate.Slow:
                return Mathf.FloorToInt(5 * Mathf.Pow(level, 3) / 4);
            default:
                return -1;
        }
    }
